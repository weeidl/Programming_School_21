#include <unistd.h>

int	spaces(char *s, int i)
{
	while (s[i] == ' ' || s[i] == '+' ||
	(9 <= s[i] && s[i] <= 13) || s[i] == '\n')
		++i;
	return (i);
}

int	ft_atoi(char *str)
{
	int i;
	int n;
	int s;

	n = 0;
	i = 0;
	s = 1;
	i = spaces(str, i);
	while (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			s = -s;
		i++;
	}
	while ('0' <= str[i] && str[i] <= '9')
	{
		n = (n + (str[i] - '0')) * 10;
		i++;
	}
	n = s * n / 10;
	return (n);
}
