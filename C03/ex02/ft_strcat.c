char	*ft_strcat(char *dest, char *src)
{
	int i;
	int s;

	i = 0;
	s = 0;
	while (src[i] != '\0')
		i++;
	while (dest[s] != '\0')
	{
		src[i + s] = dest[s];
		s++;
	}
	src[i + s] = '\0';
	return (src);
}
