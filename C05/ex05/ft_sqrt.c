int		myth(int mt)
{
	if (mt < 0)
		mt = -1 * mt;
	return (mt);
}

int		ft_sqrt(int nb)
{
	int res;

	res = 1;
	while (myth(res * res - nb) >= 2)
	{
		res = ((nb / res) + res) / 2;
	}
	return (res);
}
